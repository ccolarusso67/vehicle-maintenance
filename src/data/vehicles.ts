// Helper functions
export function getMakes(): string[] {
  return [...new Set(vehicles.map(v => v.make))].sort();
}

export function getModels(make: string): string[] {
  return [...new Set(vehicles.filter(v => v.make === make).map(v => v.model))].sort();
}

export function getVehicleVariants(make: string, model: string): VehicleData[] {
  return vehicles.filter(v => v.make === make && v.model === model);
}

export function getVehicle(make: string, model: string, engine: string): VehicleData | undefined {
  return vehicles.find(v => v.make === make && v.model === model && v.engine === engine);
}
